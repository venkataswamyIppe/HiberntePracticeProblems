package com.hibernate.pojo;

public class Product {

	private int pid;
	private int pname;
	private double price;
	
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public int getPname() {
		return pname;
	}
	public void setPname(int pname) {
		this.pname = pname;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	
}
